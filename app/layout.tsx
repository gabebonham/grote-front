  // Markdown-like formatting
  function formatText(text) {
    // Escape HTML first
    let html = text
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;");
    // Bold
    html = html.replace(/\\*\\*(.+?)\\*\\*/g, "<strong>$1</strong>");
    // Bullet points: lines starting with - or *
    html = html.replace(/^[\\-\\*]\\s+(.+)$/gm, "\u2022 $1");
    // Newlines to <br>
    html = html.replace(/\\n/g, "<br>");
    return html;
  }

